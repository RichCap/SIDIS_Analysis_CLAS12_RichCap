import argparse


def display_fit_parameters(Q2_y_Bin_Special, z_pT_Bin_Special):
    # Check if the bin exists in `special_fit_parameters_set`
    bin_key = (str(Q2_y_Bin_Special), str(z_pT_Bin_Special))
    if(bin_key in special_fit_parameters_set):
        bin_settings = special_fit_parameters_set[bin_key]
        print(f"Fit parameters for (Q2-y)-(z-pT) Bin ({Q2_y_Bin_Special}-{z_pT_Bin_Special}):")
        for param, value in bin_settings.items():
            print(f"  {param}: {value}")
    else:
        print(f"No fit parameters found for bin ({Q2_y_Bin_Special}, {z_pT_Bin_Special})")

def main():
    parser = argparse.ArgumentParser(description="Check fit parameters for specific bins in special_fit_parameters_set. In it, Parameter B refers to the Cos(phi) moment and Parameter C refers to the Cos(2*phi) moment.")
    parser.add_argument(
        "Q2_y_Bin_Special",
        type=str,
        help="The Q2-y bin number to search for (e.g., '1', '2', '3', ..., or '17')."
    )
    
    parser.add_argument(
        "z_pT_Bin_Special",
        type=str,
        help="The z-pT bin number to search for."
    )

    args = parser.parse_args()
    
    # Display fit parameters for the given bin identifiers
    display_fit_parameters(args.Q2_y_Bin_Special, args.z_pT_Bin_Special)

if(__name__ == "__main__"):
    print("")
